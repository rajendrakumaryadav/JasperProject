package in.co.dreamsdoor.beans;

public class Keywords {
	private String word;
	private String res;
	
	public Keywords(String word, String res) {
//		super();
		this.word = word;
		this.res = res;
	}

	public String getWord() {
		return word;
	}

	public void setWord(String word) {
		this.word = word;
	}

	public String getRes() {
		return res;
	}

	public void setRes(String res) {
		this.res = res;
	}
	
	
}
